public class Pet {

    String name;
    String type;

    public Pet(String name,String type)
    {
        this.name=name;
        this.type=type;
    }

    void setName(String name)
    {
        this.name=name;
    }

    void setType(String type)
    {
        this.type=type;
    }

    String getName(String name)
    {
        return name;
    }

    String getType(String type)
    {
        return type;
    }






}
